                $dateStartRepeated = \Yii::$app->formatter->asDateTime(
                    $alreadyRepeatedTasks[$key]->date_start, 'php:d.m.Y'
                );
                $dateCalcRepeated = \Yii::$app->formatter->asDateTime(
                    $alreadyRepeatedTasks[$key]->date_calculate, 'php:d.m.Y'
